"""
Шкала процента от продаж в зависимости от выполнения плана.

Вынесена в отдельную функцию, чтобы шкалу можно было легко поменять
в будущем (границы, ставки, количество уровней), не трогая остальной
код расчета зарплаты.
"""
from __future__ import annotations


def get_sales_percent_by_plan(plan_percent: float) -> float:
    """Возвращает процент от продаж, соответствующий проценту выполнения плана.

    Шкала:
        <= 80%      -> 1%
        81-90%      -> 2%
        91-109%     -> 3%
        >= 110%     -> 4% (максимум)
    """
    if plan_percent >= 110:
        return 4.0
    if plan_percent >= 91:
        return 3.0
    if plan_percent >= 81:
        return 2.0
    return 1.0
